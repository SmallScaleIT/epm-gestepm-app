package com.epm.gestepm.lib.controller.metadata;

public class OperationMetadata {

    private String apiOperationName;

    private String apiOperationDocLink;

    public String getApiOperationName() {
        return apiOperationName;
    }

    public void setApiOperationName(String apiOperationName) {
        this.apiOperationName = apiOperationName;
    }

    public String getApiOperationDocLink() {
        return apiOperationDocLink;
    }

    public void setApiOperationDocLink(String apiOperationDocLink) {
        this.apiOperationDocLink = apiOperationDocLink;
    }

}
